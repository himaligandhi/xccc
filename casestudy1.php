<p>
This is an interesting transaction because it highlights how the complexity of a deal can totally change the deal itself and the need for the financial advisor to be nimble and to be able to see and structure the correct solution.

</p>
<br>
<h4 style="color: #56565E;">
    The Ask
</h4>
<p>
<span style="color:#8B040B;">COR</span>Financial was originally engaged to provide bridge funding to a company (the “Buyer”) who had entered into a joint venture with the owner of a development site (the “Seller”) to complete a multi-purpose development in Ottawa.
</p>
<br>

<h4 style="color:#56565E;">Why <span style="color:#8B040B;">COR</span>Financial</h4>

<ol style="font-size: 18px;">
    <li><span style="color:#8B040B;">COR</span>Financial specializes in bridge loan and asset-based loan structures and also has a strategic relationship with a mortgage agent licensed through Mortgage Alliance, a licensed mortgage broker</li>
    <li>The Buyer had convinced the then current 1st mortgagee to renew their mortgage which was in arrears and needed additional funding for working capital</li>
    <li>
    The appraised value of the property did not warrant additional mortgage funding…however, there was sufficient equity in the property to structure a bridge loan
    </li>
</ol>
<h4 style="color: #56565E;">
    The Reveal and Resolution
</h4>
<p>
<span style="color:#8B040B;">COR</span>Financial obtained a financing offer for the Buyer which required the Buyer to provide financial disclosure and proof of funds to complete the financing. When <span style="color:#8B040B;">COR</span>Financial presented the offer we discovered that the Buyer had defaulted on the joint venture agreement when it failed to pay the deposit to the Seller as stipulated in the joint venture agreement and that they had no financial resources to complete the transaction…simply put, their plan was to use the joint venture agreement to take control of the property from the Seller without putting up any money and then to leverage the equity in the property to raise capital for their own use. 
</p>
<br>
<p>

<span style="color:#8B040B;">COR</span>Financial terminated its engagement and notified all parties, triggering the following:
</p>
<ol style="font-size:18px;">
<li>
	The 1st mortgagee moved forward with a power of sale to recover their money
</li>
<li>
The Seller asked <span style="color:#8B040B;">COR</span>Financial for assistance to ward off a distressed sale of the property which would have resulted in the 1st mortgagee recovering approx. 70 cents in the dollar, the 2nd mortgagee losing all of their principal and the Seller’s guarantee being exposed
</li>
</ol>
<p>
With the support of the 1st mortgagee who agreed to give <span style="color:#8B040B;">COR</span>Financial time to work through its plan, we were able to complete a plan that resulted in full recovery for both the 1st and 2nd mortgagee’s and recovery of some of the Seller’s equity:
</p>
<ol style="font-size: 18px;">
<li>
First step was to procure a bridge loan commitment to show the 1st mortgagee that, if need be, we could pay out their mortgage
</li>
<li>
We then created a joint venture structure, which included the bridge loan funding, which we used to negotiate with well-heeled developers in the Ottawa area
</li>
<li>
This process led to a fair market value cash offer from a respected developer group in Ottawa
</li>
<li>
The sale closed at the end of April, 2020
</li>
</ol>
<h4 style="color: #56565E;">
Conclusion/Closing Thoughts
</h4>
<p>
This transaction hi-lights the need for financial consultants, lenders, mortgage brokers, etc. to ensure that they:
</p>
<ol style="font-size: 18px">
<li>
Ask the right questions…better to duplicate questions than to leave anything out.
</li>
<li>
Do PROPER due diligence on all parties, and
</li>
<li>
Create the CORRECT funding structure that accurately represents both borrower and lender needs/criteria.
</li>
</ol>
<p>
The reason for <span style="color:#8B040B;">COR</span>Financial’s successful track record is that we <strong>PRACTICE WHAT WE PREACH!</strong> 
